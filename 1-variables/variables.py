# Multiple variables
x, y, z = 10, 20, 30
print(x, y, z) 

e, r, k, a, n = 10, 20, 30, 40, 50
print(e, r, k, a, n)

# Multiple variables
a = b = c = 5
print(a, b, c) 

d = e = r
print(d, e, r)

# f-string
