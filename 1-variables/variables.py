# Python Variables Examples


# 1. Basic variables
name = "Erkan"    # String variable
age = 25          # Integer variable
height = 1.75  # Float variable
is_student = True  # Boolean variable

""" String (str) = Stores text, like words or sentences.

Integer (int) = Stores whole numbers, without decimals.

Float (float) = Stores numbers with decimals.

Boolean (bool) = Stores True or False, used for conditions."""

# Printing variables 
print("Name:", name)
print("Age:", age)
print("Height:", height)
print("Is Student:", is_student)

# Input() always returns a string (text)!
name = input("Enter your name: ")
age = input("Enter your age: ")

print("Hello", name)
print("Your age", age)

# Variables
name = "Alice"
